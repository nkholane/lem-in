#include "libft.h"

int		ft_isalpha(int c)
{
	int result;

	if (c <= 'Z' && c >= 'A')
		result = 1;
	else if (c <= 'z' && c >= 'a')
		result = 1;
	else
		result = 0;
	return (result);
}
