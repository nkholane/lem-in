#include "libft.h"

int		ft_isalnum(int c)
{
	int result;

	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		result = 1;
	else if (c >= '0' && c <= '9')
		result = 1;
	else
		result = 0;
	return (result);
}
